package Hardware;

public class Mainboard {
    String name;
    String hersteller;
    String formfaktor;
    String chipsatz;
    String ram;

    Mainboard(String name, String hersteller, String formfaktor, String chipsatz, String ram) {
        this.name = name;
        this.hersteller = hersteller;
        this.formfaktor = formfaktor;
        this.chipsatz = chipsatz;
        this.ram = ram;
    }
}
